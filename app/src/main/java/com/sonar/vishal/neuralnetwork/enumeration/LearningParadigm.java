package com.sonar.vishal.neuralnetwork.enumeration;

/*
    SUPERVISED: Learning type with pairs of independent values and dependent values
                with the objective to map them in a function.
    UNSUPERVISED: Learning type deal only with data without any labelling or classification.
                  One tries to make an inference and extract knowledge be taking into account
                  only the independent data.
 */
public enum LearningParadigm {

    SUPERVISED, UNSUPERVISED
}
